import UIKit

extension UIColor {
    
    //UIView Colors
    class var customViewRedColor: UIColor {
        return UIColor(named: "customViewRedColor") ?? .red
    }

    class var customViewGreyColor: UIColor {
        return UIColor(named: "customViewGreyColor") ?? .red
    }
}
